(function() {
	'use strict';

	var dietary = [
		'Gluten-free',
		'Vegan',
		'Vegetarian'
	];

	var insertChar = [
		'⅛',
		'¼',
		'⅓',
		'½',
		'⅔',
		'¾'
	];

	var categories = [
		'Appetizer',
		'Beverage',
		'Dessert',
		'Entree',
		'Salad',
		'Side',
		'Soup'
	];

	var tags = [
		'alcohol',
		'baked',
		'beef',
		'confection',
		'fast',
		'fish',
		'low-calorie',
		'one-pot',
		'pasta',
		'pork',
		'poultry',
		'slow-cook',
		'stock',
		'vegetable'
	];

	angular
		.module('rBox')
		.factory('Recipe', Recipe);

	function Recipe() {
		// callable members
		return {
			dietary: dietary,
			insertChar: insertChar,
			categories: categories,
			tags: tags,
			generateId: generateId
		};

		/**
		 * Generates a unique 5-character ID;
		 *
		 * @returns {string}
		 */
		function generateId() {
			var _id = '';
			var _charset = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
			var i;

			for (i = 0; i < 5; i++) {
				_id += _charset.charAt(Math.floor(Math.random() * _charset.length));
			}

			return _id;
		}
	}
}());